    private void fetch() throws SQLException {
        con = DriverManager.getConnection(url, username, password);
        
        int q;
        pst = con.prepareStatement("SELECT * FROM mahasiswa");
        res = pst.executeQuery();
        ResultSetMetaData results = res.getMetaData();
        q = results.getColumnCount();
        
        DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
        df.setRowCount(0);
        while(res.next()) {
            Vector v2 = new Vector();
            for(int a = 1; a <= q; a++) {
                v2.add(res.getString("id"));
                v2.add(res.getString("npm"));
                v2.add(res.getString("nama"));
                v2.add(res.getString("jurusan"));
                v2.add(res.getString("alamat"));
                v2.add(res.getString("phone"));
            }
            df.addRow(v2);
        }
    }

