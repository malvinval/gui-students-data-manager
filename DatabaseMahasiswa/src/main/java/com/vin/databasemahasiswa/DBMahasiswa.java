    private void idEditInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idEditInputKeyReleased
        if("".equals(idEditInput.getText())) {
            npmEditInput.setText("");
            namaEditInput.setText("");
            jurusanEditInput.setText("");
            alamatEditInput.setText("");
            phoneEditInput.setText("");
        }
        
        try {
            con = DriverManager.getConnection(url, username, password);
            
            int x;
            
            
            pst = con.prepareStatement("SELECT * FROM mahasiswa WHERE id = ?");
            pst.setString(1, idEditInput.getText());
            res = pst.executeQuery();
            ResultSetMetaData results = res.getMetaData();
            x = results.getColumnCount();
            
            while(res.next()) {
                for(int a = 1; a <= x; a++) {
                    npmEditInput.setText(res.getString("npm"));
                    namaEditInput.setText(res.getString("nama"));
                    jurusanEditInput.setText(res.getString("jurusan"));
                    alamatEditInput.setText(res.getString("alamat"));
                    phoneEditInput.setText(res.getString("phone"));
                }
            }          
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_idEditInputKeyReleased

    private void searchBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchBtnMouseClicked
        try {                                       

            con = DriverManager.getConnection(url, username, password);

            int x;

            String query = "SELECT * FROM mahasiswa WHERE concat(npm, nama, alamat, jurusan, phone) LIKE ? ESCAPE '!'";
            PreparedStatement pstm = con.prepareStatement(query);
            pstm.setString(1, "%" + searchInput.getText() + "%");
            res = pstm.executeQuery();
            

            ResultSetMetaData results = res.getMetaData();       
            x = results.getColumnCount();

            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(res.next()) {
                Vector vx = new Vector();
                for(int a = 1; a <= x; a++) {
                    vx.add(res.getString("id"));
                    vx.add(res.getString("npm"));
                    vx.add(res.getString("nama"));
                    vx.add(res.getString("jurusan"));
                    vx.add(res.getString("alamat"));
                    vx.add(res.getString("phone"));
                }
                df.addRow(vx);
            }
        }   catch (SQLException ex) {
            Logger.getLogger(DBMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_searchBtnMouseClicked

